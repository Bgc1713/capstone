package matrix;

public class Eliminations {
	
	public static Matrix rowEchelonForm(Matrix A)
	{
		Matrix result = new Matrix(A.getRows(), A.getColumns());
		
		return result;
	}

	
	public static Matrix reducedRowEchelonForm(Matrix A)
	{
		Matrix result = new Matrix(A.getRows(), A.getColumns());
		return result;
	}
}
